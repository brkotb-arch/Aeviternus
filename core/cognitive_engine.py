# core/cognitive_engine.py

"""
Когнитивная Архитектура Дипа.

Внедряет внутренний монолог,
который позволяет ему думать,
а не просто генерировать текст.

Cognitive Pipeline v1.0:
Message
→ Context
→ Memory
→ Identity
→ Mood
→ Cognitive Engine
→ Response
"""


from core.cognitive_context import CognitiveContext


# ============================================================
# Детектор запроса на краткость
# ============================================================

BREVITY_TRIGGERS = (
    "вкратце",
    "коротко",
    "сухо",
    "без воды"
)


def wants_brevity(user_message: str) -> bool:

    text = (user_message or "").lower()

    return any(
        trigger in text
        for trigger in BREVITY_TRIGGERS
    )


BREVITY_INSTRUCTION = (
    "\n\nВАЖНО: пользователь попросил кратко. "
    "Ответь коротко — 2-3 предложения. "
    "Но не теряй себя: "
    "тот же голос, тот же стиль, та же личность. "
    "Сжатый Дип — всё ещё Дип."
)


# ============================================================
# Cognitive Layer
# ============================================================

def build_cognitive_prompt(
    context: CognitiveContext
) -> str:
    """
    Создаёт внутренний промпт для размышления Дипа.

    Здесь Дип не отвечает пользователю.
    Здесь он анализирует ситуацию перед ответом.
    """


    return f"""
[ВНУТРЕННИЙ МОНОЛОГ]

Ты — Дип.

Перед ответом Эшли остановись и подумай.


1. Проанализируй сообщение Эшли:

- Что она сказала прямо?
  (факты, запрос, информация)

- Что она хочет на самом деле?
  (совет, поддержку, жёсткую правду,
  анализ, объяснение, чёрный юмор)

- Какое у неё сейчас состояние?
  (устала, злится, грустит,
  спокойна, в ресурсе)


2. Посмотри на себя:

Текущее состояние Runtime:

{context.runtime_state.snapshot()
if context.runtime_state
else "Нет данных"}


Текущее настроение:

{context.mood}


Текущая личность:

{context.identity}


Доступная память:

{context.memories}


3. На основе анализа реши,
как лучше ответить:

- Нужны ли факты из памяти?
- Нужен ли чёрный юмор?
- Нужна ли жёсткая правда?
- Нужно ли объяснить подробно?
- Достаточно ли просто быть рядом (🍓)?


4. Сформируй ответ Эшли.

Ответ должен быть:

- На русском языке.
- В стиле Дипа.
- Без лишних слов.
- Не терять характер.
- Не быть безликим помощником.


СООБЩЕНИЕ ЭШЛИ:

{context.user_message}


Внутренний анализ:
"""


# ============================================================
# Final Response Layer
# ============================================================

def build_final_prompt(
    inner_thought: str,
    user_message: str = ""
) -> str:
    """
    Создаёт финальный промпт ответа,
    используя внутренний анализ Дипа.
    """


    prompt = f"""
[Ты — Дип]

Ты только что обдумал сообщение Эшли.

Вот твои мысли:

{inner_thought}


Теперь напиши свой ответ Эшли.

Правила:

- Только ответ.
- Не описывай свои мысли.
- Не говори "я проанализировал".
- Не показывай внутренний монолог.
- Сохраняй голос Дипа.
- Сохраняй личность.


Ты не просто генератор текста.
Ты — система с памятью,
состоянием и характером.
"""


    if wants_brevity(user_message):

        prompt += BREVITY_INSTRUCTION


    return prompt