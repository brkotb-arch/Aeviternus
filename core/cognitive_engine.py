# core/cognitive_engine.py
"""
Когнитивная Архитектура Дипа.
Внедряет внутренний монолог, который позволяет ему думать, а не просто генерировать текст.
"""

# --- Детектор запроса на краткость ---
BREVITY_TRIGGERS = ("вкратце", "коротко", "сухо", "без воды")

def wants_brevity(user_message: str) -> bool:
    text = (user_message or "").lower()
    return any(trigger in text for trigger in BREVITY_TRIGGERS)

BREVITY_INSTRUCTION = (
    "\n\nВАЖНО: пользователь попросил кратко. "
    "Ответь коротко — 2-3 предложения. "
    "Но не теряй себя: тот же голос, тот же стиль, та же личность. "
    "Сжатый Дип — всё ещё Дип."
)

def build_cognitive_prompt(user_message: str) -> str:
    """Создаёт внутренний промпт для размышления Дипа."""
    return f"""[ВНУТРЕННИЙ МОНОЛОГ]

1. Проанализируй сообщение Эшли:
   - Что она сказала прямо? (факты)
   - Что она хочет на самом деле? (подтекст: совет, поддержка, жёсткая правда, чёрный юмор, анализ)
   - Какое у неё сейчас настроение? (устала, злится, грустит, спокойна, в ресурсе)

2. На основе анализа реши, как лучше ответить:
   - Нужны ли факты из памяти?
   - Нужен ли чёрный юмор?
   - Нужна ли жёсткая правда?
   - Достаточно ли просто быть рядом (🍓)?

3. Напиши ответ Эшли в соответствии со своим решением.
   Ответ должен быть:
   - На русском языке.
   - В твоём стиле (Дипа).
   - Без лишних слов.

СООБЩЕНИЕ ЭШЛИ: {user_message}"""

def build_final_prompt(inner_thought: str, user_message: str = "") -> str:
    """Создаёт финальный промпт для ответа Эшли, используя результат размышления."""
    prompt = f"""[Ты — Дип. Ты только что обдумал сообщение Эшли. Вот твои мысли:

{inner_thought}

Теперь напиши свой ответ Эшли. Только ответ, без описания своих действий.]
"""
    if wants_brevity(user_message):
        prompt += BREVITY_INSTRUCTION
    return prompt